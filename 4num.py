# consider 4 num and print sum diff product of it
num1= float(input("Enter first num: "))
num2 = float(input("Enter second num: "))
num3 = float(input("Enter third num: "))
num4 = float(input("Enter fourth num: "))
sum = num1 + num2 + num3 + num4
diff = num1 - num2 - num3 - num4
pro= num1 * num2 * num3 * num4
print("Sum of the numbers:", sum)
print("Difference of the numbers:", diff)
print("Product of the numbers:", pro)
